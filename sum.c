// Write a program to input two numbers and display their sum.
#include <stdio.h>
int main()
{
    int a;
    int b;
    printf("Enter number in a: ");
    scanf("%d", & a);
    printf("Enter number in b: ");
    scanf("%d", & b);
    int sum = a + b;
    printf("sum is = %d\n", sum);
    return 0;
}


